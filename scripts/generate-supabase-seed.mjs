function venuesSql() {
  const names = [...new Set(fixtures.map((fixture) => fixture.venue))].sort();
  const values = names.map((name) => {
    const meta = venueMeta[name] ?? {
      city: name,
      region: null,
      countryCode: "USA",
      timezone: "America/New_York"
    };

    return `(${literal(venueId(name))}, ${literal(name)}, ${literal(meta.city)}, ${literal(meta.region)}, ${literal(
      meta.countryCode
    )}, ${literal(meta.timezone)})`;
  });

  return [
    "insert into public.venues (id, name, city, region, country_code, timezone)",
    `values\n  ${values.join(",\n  ")}`,
    "on conflict (id) do update set",
    "  name = excluded.name,",
    "  city = excluded.city,",
    "  region = excluded.region,",
    "  country_code = excluded.country_code,",
    "  timezone = excluded.timezone;"
  ].join("\n");
}

function teamsSql() {
  const values = teams.map((team) =>
    `(${literal(team.id)}, ${literal(team.id)}, ${literal(team.name)}, ${literal(team.name)}, ${literal(
      team.id
    )}, ${literal(team.id)}, ${literal(team.group)}, ${team.rating}, ${literal(flagEmoji[team.id] ?? null)})`
  );

  return [
    "insert into public.teams (id, fifa_code, name, official_name, short_name, country_code, group_code, rating, flag_emoji)",
    `values\n  ${values.join(",\n  ")}`,
    "on conflict (id) do update set",
    "  fifa_code = excluded.fifa_code,",
    "  name = excluded.name,",
    "  official_name = excluded.official_name,",
    "  short_name = excluded.short_name,",
    "  country_code = excluded.country_code,",
    "  group_code = excluded.group_code,",
    "  rating = excluded.rating,",
    "  flag_emoji = excluded.flag_emoji;"
  ].join("\n");
}

function fixturesSql() {
  const values = fixtures.map((fixture, index) => {
    const homeGoals = Number.isFinite(fixture.homeGoals) ? fixture.homeGoals : null;
    const awayGoals = Number.isFinite(fixture.awayGoals) ? fixture.awayGoals : null;
    const winnerTeamId = getWinnerTeamId(fixture);

    return `(${literal(fixture.id)}, ${index + 1}, ${literal(fixture.group)}, 'group', ${literal(
      fixture.homeTeamId
    )}, ${literal(fixture.awayTeamId)}, ${literal(toUtcIso(fixture.kickoff))}, ${literal(venueId(fixture.venue))}, ${literal(
      fixture.venue
    )}, ${literal(statusMap[fixture.status] ?? "scheduled")}, ${numberOrNull(homeGoals)}, ${numberOrNull(
      awayGoals
    )}, ${literal(winnerTeamId)}, ${fixture.status === "FT" ? "now()" : "null"}, 'manual_seed')`;
  });

  return [
    "insert into public.fixtures (id, match_number, group_code, stage, home_team_id, away_team_id, kickoff_at, venue_id, venue_name, status, home_goals, away_goals, winner_team_id, result_verified_at, source)",
    `values\n  ${values.join(",\n  ")}`,
    "on conflict (id) do update set",
    "  match_number = excluded.match_number,",
    "  group_code = excluded.group_code,",
    "  stage = excluded.stage,",
    "  home_team_id = excluded.home_team_id,",
    "  away_team_id = excluded.away_team_id,",
    "  kickoff_at = excluded.kickoff_at,",
    "  venue_id = excluded.venue_id,",
    "  venue_name = excluded.venue_name,",
    "  status = excluded.status,",
    "  home_goals = excluded.home_goals,",
    "  away_goals = excluded.away_goals,",
    "  winner_team_id = excluded.winner_team_id,",
    "  result_verified_at = excluded.result_verified_at,",
    "  source = excluded.source;"
  ].join("\n");
}

function matchEventsSql() {
  const events = fixtures.flatMap((fixture) =>
    fixture.scorers.map((scorer) => ({
      fixtureId: fixture.id,
      teamId: scorer.teamId,
      player: scorer.player,
      minute: scorer.minute
    }))
  );

  if (!events.length) {
    return "-- No match events in seed.";
  }

  const values = events.map(
    (event) =>
      `(${literal(event.fixtureId)}, ${literal(event.teamId)}, ${literal(event.player)}, ${event.minute}, 'goal', 'manual_seed')`
  );

  return [
    "delete from public.match_events where source = 'manual_seed';",
    "insert into public.match_events (fixture_id, team_id, player_name, minute, event_type, source)",
    `values\n  ${values.join(",\n  ")};`
  ].join("\n");
}

function getWinnerTeamId(fixture) {
  if (fixture.status !== "FT" || !Number.isFinite(fixture.homeGoals) || !Number.isFinite(fixture.awayGoals)) {
    return null;
  }

  if (fixture.homeGoals > fixture.awayGoals) return fixture.homeTeamId;
  if (fixture.awayGoals > fixture.homeGoals) return fixture.awayTeamId;
  return null;
}

function venueId(name) {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
}

function toUtcIso(value) {
  return new Date(value).toISOString();
}

function literal(value) {
  if (value === null || value === undefined) return "null";
  return `'${String(value).replaceAll("'", "''")}'`;
}

function numberOrNull(value) {
  return Number.isFinite(value) ? String(value) : "null";
}
